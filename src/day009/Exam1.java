    // 조류 클래스는 동물 클래스로 부터 상속 받는다. 동물로 부터 조류가 상속을 받았다.

class 참새 extends 조류{}
    // 참새 클래스는 조류 클래스로 부터 상속 받는다.

class 닭 extends 조류{}
    // 하위 클래스는 주로 클래스로 부터 상속 받는다.


